        $column_order = array('hukum.tgl_produkh','hukum.nama_produkh','hukum.file_hukum','hukum.status','hukum.uraianh',null);
        $column_search = array('hukum.tgl_produkh','hukum.nama_produkh','hukum.status','hukum.uraianh',null);
